# Operadores aritmeticos

a = 5
b = 3

# Soma
print("Soma: ", a + b)

# Subtração 
print("Subtração: ", a - b)

# Multiplicação 
print("Multiplicação: ", a * b)

# Divisão
print("Divisão: ", a / b)

# Divisão inteira
print("Divisão Inteira; ", a // b)

# Módulo / Resto da divisão

print("Módulo: ", a % b)

# Exponenciação 
print("Exponenciação: ", a ** b)

