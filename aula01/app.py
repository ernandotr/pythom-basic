# String
nome = "Ernando"
print("Nome:", nome)

#Integer
idade = 18
print ("Idade:", idade)

# Float
altura = 1.81
print("Altura:", altura)

#Boolean
situacao = True
print("Situação:", situacao)

# List
numeros =   [2, 3, 5, 7, 11]
print("Números:", numeros)
print("Número:", numeros[2])

# Dictionaries
usuario = {
    "nome": "Ernando",
    "idade": 18,
    "cidade": "Oeiras"

}
print(usuario)
print("Cidade:", usuario["cidade"])

#Tuples (immutable)
curso = ("Curso de Python e Django", "Cesar", 2024, 197.52)
print("Título:", curso[0])